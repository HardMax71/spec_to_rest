import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const SPEC_DIR = path.resolve(__dirname, "..", "..", "fixtures", "spec");
const OUT_FILE = path.resolve(__dirname, "..", "lib", "playground-examples.generated.ts");

const MAX_BYTES = 16 * 1024;

const EXCLUDE = new Set([
  "broken_decrement.spec",
  "broken_url_shortener.spec",
  "convention_errors.spec",
  "contradictory_powerset.spec",
  "dead_op.spec",
  "strict_strategies_negative.spec",
  "unsat_invariants.spec",
]);

const PREFERRED_ORDER = [
  "safe_counter.spec",
  "url_shortener.spec",
  "todo_list.spec",
  "auth_service.spec",
  "ecommerce.spec",
  "edge_cases.spec",
];

if (!fs.existsSync(SPEC_DIR)) {
  console.error(`✘ playground-examples: ${SPEC_DIR} does not exist`);
  process.exit(1);
}

const all = fs
  .readdirSync(SPEC_DIR)
  .filter((f) => f.endsWith(".spec") && !EXCLUDE.has(f))
  .filter((f) => {
    const size = fs.statSync(path.join(SPEC_DIR, f)).size;
    return size > 0 && size <= MAX_BYTES;
  });

const ordered = [
  ...PREFERRED_ORDER.filter((f) => all.includes(f)),
  ...all.filter((f) => !PREFERRED_ORDER.includes(f)).sort(),
];

const examples = ordered.map((file) => {
  const name = file.replace(/\.spec$/, "");
  const spec = fs.readFileSync(path.join(SPEC_DIR, file), "utf8");
  return { name, spec };
});

const banner =
  "// AUTO-GENERATED by docs/scripts/build-playground-examples.mjs.\n" +
  "// Source: fixtures/spec/*.spec. Re-run `npm run prebuild` to regenerate.\n\n";

const body =
  "export type PlaygroundExample = { name: string; spec: string };\n\n" +
  `export const PLAYGROUND_EXAMPLES: readonly PlaygroundExample[] = ${JSON.stringify(examples, null, 2)} as const;\n`;

fs.mkdirSync(path.dirname(OUT_FILE), { recursive: true });
fs.writeFileSync(OUT_FILE, banner + body, "utf8");

console.log(
  `✓ playground-examples: ${examples.length} specs written to ${path.relative(process.cwd(), OUT_FILE)}`,
);
